    private void actualizar(){
        solicitudesNoAtendidas = facade.obtenerListaSolicitudesNoAtendidas();
        renderizarTablas();
        actualizarColumnas();
    }
    
    private void renderizarTablas() {
        vaciarTablas();
        DefaultTableModel modeloTablaSolicitudes = (DefaultTableModel) tablaSolicitudes.getModel();
        Object rowData[] = new Object[3];

        for (int i = 0; i < solicitudesNoAtendidas.size(); i++) {
            rowData[0] = facade.obtenerEmpresaProductora(solicitudesNoAtendidas.get(i).getRFCEmpresa()).getNombreProductora();
            rowData[1] = solicitudesNoAtendidas.get(i).getFecha();
            
            List<Residuo> residuosTransportar = new ArrayList<>();
            for (int j = 0; j < solicitudesNoAtendidas.get(i).getDetalles().size(); j++) {
                residuosTransportar.add(facade.obtenerResiduo(solicitudesNoAtendidas.get(i).getDetalles().get(j).getIDResiduo()));
            }
            
            rowData[2] = residuosTransportar.toString();
            modeloTablaSolicitudes.addRow(rowData);
        }
    }

    private void vaciarTablas() {
        while (tablaSolicitudes.getRowCount() > 0) {
            ((DefaultTableModel) tablaSolicitudes.getModel()).removeRow(0);
        }
    }
    
    private void actualizarColumnas() {
        scroll.getViewport().setBackground(Color.WHITE);
        tablaSolicitudes.getTableHeader().setReorderingAllowed(false);
        tablaSolicitudes.getTableHeader().setResizingAllowed(true);

        Font f = new Font("Seoge UI", Font.BOLD, 15);
        tablaSolicitudes.getTableHeader().setFont(f);
        
        tablaSolicitudes.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);

        for (int i = 0; i < tablaSolicitudes.getColumnCount(); i++) {
            DefaultTableColumnModel colModel = (DefaultTableColumnModel) tablaSolicitudes.getColumnModel();
            TableColumn col = colModel.getColumn(i);
            int width = 0;

            TableCellRenderer renderer = col.getHeaderRenderer();
            for (int r = 0; r < tablaSolicitudes.getRowCount(); r++) {
                renderer = tablaSolicitudes.getCellRenderer(r, i);
                Component comp = renderer.getTableCellRendererComponent(tablaSolicitudes, tablaSolicitudes.getValueAt(r, i),
                        false, false, r, i);
                width = Math.max(width, comp.getPreferredSize().width);
            }
            col.setPreferredWidth(width + 20);
        }
    }

