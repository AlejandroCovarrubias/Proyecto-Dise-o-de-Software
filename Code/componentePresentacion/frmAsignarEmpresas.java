    private void asignarTrasladoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_asignarTrasladoActionPerformed
        int[] selectedRows = tablaEmpresasTransporte.getSelectedRows();
        
        if(selectedRows.length > 0){
            try {
                Solicitud solicitud = facade.obtenerSolicitud(String.valueOf(detalles.getIDSolicitud()));
                float cantidadReparto = detalles.getCantidad() / selectedRows.length;
                detalles.setCantidad(0);
                
                for (int selectedRow : selectedRows) {
                    facade.agregarAsignacion(solicitud.getIDSolicitud(), empresas.get(selectedRow).getCodigoAcceso(), detalles.getIDResiduo(), cantidadReparto, detalles.getMedida());
                }
                JOptionPane.showMessageDialog(this, "Asignaciones realizadas", "Asignaciones terminadas", JOptionPane.PLAIN_MESSAGE);
                this.setVisible(false);
            } catch (ResiduosException residuosException) {
                JOptionPane.showMessageDialog(this, residuosException.getMessage(), "Error al realizar la asignacion", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione una o mas empresas", "No se han seleccionado empresa", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_asignarTrasladoActionPerformed

