    private void botonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarActionPerformed
        String codigo = this.rfcText.getText();
        String pass = String.valueOf(this.passText.getPassword());
        int tipoLog = 0;
        String nombre = this.nombreText.getText();
        
        if (botonProductora.isSelected()) {
            tipoLog = 2;
        } else if (botonTransporte.isSelected()) {
            tipoLog = 3;
        } else {
            JOptionPane.showMessageDialog(this, "Especifique un tipo de usuario", "No especifico tipo de usuario", JOptionPane.PLAIN_MESSAGE);
        }
        
        if(tipoLog != 0){
            if(facade.registrarUsuario(codigo, pass, tipoLog, nombre)){
                JOptionPane.showMessageDialog(this, "Se ha registrado con exito", "Registrado", JOptionPane.PLAIN_MESSAGE);
                frmInicioSesion inicioSesion = frmInicioSesion.getInstance();
                inicioSesion.setVisible(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "No se pudo registrar al usuario. Vuelva a intentarlo", "Registro fallido", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_botonRegistrarActionPerformed

