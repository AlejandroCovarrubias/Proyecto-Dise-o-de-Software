    private void actualizar(){
        try {
            elementosTabla = new ArrayList<>(facade.obtenerListaElementosQuimicos());
        } catch (ResiduosException e) {
            throw new ResiduosException("No se pudieron obtener los elementos quimicos");
        }
        elementosNuevoResiduo = new ArrayList<>();
        codigoText.setText("");
        nombreText.setText("");
        renderizarTablas();
    }
    
    private void renderizarTablas() {
        vaciarTablas();
        DefaultTableModel modeloTablaElementos = (DefaultTableModel) tablaElementos.getModel();
        Object rowData[] = new Object[1];

        for (int i = 0; i < elementosTabla.size(); i++) {
            rowData[0] = elementosTabla.get(i).getNombreElemento();
            modeloTablaElementos.addRow(rowData);
        }

        DefaultTableModel modeloTablaNuevoResiduo = (DefaultTableModel) tablaNuevosResiduos.getModel();
        for (int i = 0; i < elementosNuevoResiduo.size(); i++) {
            rowData[0] = elementosNuevoResiduo.get(i).getNombreElemento();
            modeloTablaNuevoResiduo.addRow(rowData);
        }
    }

    private void vaciarTablas() {
        while (tablaElementos.getRowCount() > 0) {
            ((DefaultTableModel) tablaElementos.getModel()).removeRow(0);
        }

        while (tablaNuevosResiduos.getRowCount() > 0) {
            ((DefaultTableModel) tablaNuevosResiduos.getModel()).removeRow(0);
        }
    }

