    private void botonRegistrarResiduoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarResiduoActionPerformed
        String codigo = codigoText.getText();
        String nombre = nombreText.getText();
        String mensajeComprobacion = "";

        if (codigo.equals("")) {
            mensajeComprobacion += "El campo de codigo esta vacio.\n";
        }

        if (nombre.equals("")) {
            mensajeComprobacion += "El campo de nombre esta vacio.\n";
        }

        if (elementosNuevoResiduo.isEmpty()) {
            mensajeComprobacion += "La lista de elementos del nuevo residuo esta vacia.";
        }

        if (mensajeComprobacion.equals("")) {
            try {
                EmpresaProductora empresa = new EmpresaProductora(usuario.getCodigoAcceso(), usuario.getNombre(), usuario.getTipo(), usuario.getNombre());
                facade.agregarResiduo(codigo, nombre, elementosNuevoResiduo, empresa);
                JOptionPane.showMessageDialog(this, "Residuo agregado con exito", "Residuo Agregado", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
            } catch (ResiduosException residuosException) {
                JOptionPane.showMessageDialog(this, residuosException.getMessage(), "Error al agregar residuo", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, mensajeComprobacion, "Error de registro", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_botonRegistrarResiduoActionPerformed

    private void agregarElementoNuevoResiduoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarElementoNuevoResiduoActionPerformed
        if (tablaElementos.getRowCount() > 0) {
            int filaSeleccionada = tablaElementos.getSelectedRow();
            if (filaSeleccionada >= 0) {
                elementosNuevoResiduo.add(elementosTabla.get(filaSeleccionada));
                elementosTabla.remove(filaSeleccionada);
                renderizarTablas();
            } else {
                JOptionPane.showMessageDialog(this, "Seleccione un elemento de la tabla de Elementos");
            }
        } else {
            JOptionPane.showMessageDialog(this, "No hay elementos en la tabla de Elementos");
        }
    }//GEN-LAST:event_agregarElementoNuevoResiduoActionPerformed

    private void quitarElementoNuevoResiduoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_quitarElementoNuevoResiduoActionPerformed
        if (tablaNuevosResiduos.getRowCount() > 0) {
            int filaSeleccionada = tablaNuevosResiduos.getSelectedRow();
            if (filaSeleccionada >= 0) {
                elementosTabla.add(elementosNuevoResiduo.get(filaSeleccionada));
                elementosNuevoResiduo.remove(filaSeleccionada);
                renderizarTablas();
            } else {
                JOptionPane.showMessageDialog(this, "Seleccione un elemento de la tabla de Elementos del Nuevo Residuo");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Agregue elementos a la tabla del Nuevo Residuo");
        }
    }//GEN-LAST:event_quitarElementoNuevoResiduoActionPerformed

